func generateTypes(cfg Config, ds *dsInfo) string {
	var b strings.Builder
	fmt.Fprintf(&b, "/*\n%s\n*/\n\n// Code generated by generate-observed; DO NOT EDIT.\n\npackage %s\n\n", cfg.CopyrightHeader, cfg.APIVersion)
	b.WriteString(`import (
	xpv1 "github.com/crossplane/crossplane-runtime/v2/apis/common/v1"
	v2 "github.com/crossplane/crossplane-runtime/v2/apis/common/v2"
	metav1 "k8s.io/apimachinery/pkg/apis/meta/v1"
	"k8s.io/apimachinery/pkg/runtime/schema"
)

`)

	// Emit nested struct types first (collected from all fields).
	emitNestedStructs(&b, ds.forProviderFields)
	emitNestedStructs(&b, ds.atProviderFields)

	// Parameters struct.
	fmt.Fprintf(&b, "// %sParameters defines the input parameters for the %s data source.\n", ds.kindName, ds.tfName)
	fmt.Fprintf(&b, "type %sParameters struct {\n", ds.kindName)
	for _, f := range ds.forProviderFields {
		writeFieldDoc(&b, f)
		if f.required {
			b.WriteString("\t// +kubebuilder:validation:Required\n")
		} else {
			b.WriteString("\t// +optional\n")
		}
		omit := ",omitempty"
		if f.required {
			omit = ""
		}
		fmt.Fprintf(&b, "\t%s %s `json:\"%s%s\"`\n\n", f.goName, f.goType, f.jsonName, omit)
	}
	b.WriteString("}\n\n")

	// Observation struct.
	fmt.Fprintf(&b, "// %sObservation holds the observed (computed) fields from the %s data source.\n", ds.kindName, ds.tfName)
	fmt.Fprintf(&b, "type %sObservation struct {\n", ds.kindName)
	for _, f := range ds.atProviderFields {
		writeFieldDoc(&b, f)
		fmt.Fprintf(&b, "\t%s %s `json:\"%s,omitempty\"`\n\n", f.goName, f.goType, f.jsonName)
	}
	b.WriteString("}\n\n")

	// Spec and Status.
	fmt.Fprintf(&b, "type %sSpec struct {\n\tv2.ManagedResourceSpec `json:\",inline\"`\n\tForProvider %sParameters `json:\"forProvider\"`\n}\n\n", ds.kindName, ds.kindName)
	fmt.Fprintf(&b, "type %sStatus struct {\n\txpv1.ResourceStatus `json:\",inline\"`\n\tAtProvider %sObservation `json:\"atProvider,omitempty\"`\n}\n\n", ds.kindName, ds.kindName)

	// CRD type with markers.
	bt := "`"
	b.WriteString("// +kubebuilder:object:root=true\n")
	b.WriteString("// +kubebuilder:subresource:status\n")
	b.WriteString("// +kubebuilder:resource:scope=Namespaced,categories=crossplane\n")
	b.WriteString("// +kubebuilder:printcolumn:name=\"READY\",type=\"string\",JSONPath=\".status.conditions[?(@.type=='Ready')].status\"\n")
	b.WriteString("// +kubebuilder:printcolumn:name=\"SYNCED\",type=\"string\",JSONPath=\".status.conditions[?(@.type=='Synced')].status\"\n")
	b.WriteString("// +kubebuilder:printcolumn:name=\"AGE\",type=\"date\",JSONPath=\".metadata.creationTimestamp\"\n\n")
	fmt.Fprintf(&b, "// %s is an observe-only resource backed by the %s Terraform data source.\n", ds.kindName, ds.tfName)
	fmt.Fprintf(&b, "type %s struct {\n", ds.kindName)
	fmt.Fprintf(&b, "\tmetav1.TypeMeta %sjson:\",inline\"%s\n", bt, bt)
	fmt.Fprintf(&b, "\tmetav1.ObjectMeta %sjson:\"metadata,omitempty\"%s\n\n", bt, bt)
	fmt.Fprintf(&b, "\tSpec   %sSpec   %sjson:\"spec\"%s\n", ds.kindName, bt, bt)
	fmt.Fprintf(&b, "\tStatus %sStatus %sjson:\"status,omitempty\"%s\n", ds.kindName, bt, bt)
	b.WriteString("}\n\n")

	// List type.
	b.WriteString("// +kubebuilder:object:root=true\n\n")
	fmt.Fprintf(&b, "// %sList contains a list of %s.\n", ds.kindName, ds.kindName)
	fmt.Fprintf(&b, "type %sList struct {\n", ds.kindName)
	fmt.Fprintf(&b, "\tmetav1.TypeMeta %sjson:\",inline\"%s\n", bt, bt)
	fmt.Fprintf(&b, "\tmetav1.ListMeta %sjson:\"metadata,omitempty\"%s\n", bt, bt)
	fmt.Fprintf(&b, "\tItems []%s %sjson:\"items\"%s\n", ds.kindName, bt, bt)
	b.WriteString("}\n\n")

	// GVK variable and init.
	fmt.Fprintf(&b, "var %s_GroupVersionKind = schema.GroupVersionKind{\n\tGroup: CRDGroup,\n\tVersion: CRDVersion,\n\tKind: %q,\n}\n\n", ds.kindName, ds.kindName)
	fmt.Fprintf(&b, "func init() {\n\tSchemeBuilder.Register(&%s{}, &%sList{})\n}\n", ds.kindName, ds.kindName)

	return b.String()
}

// emitNestedStructs recursively emits Go struct definitions for fields that
// have nested sub-fields (e.g. TypeList/TypeSet with Resource elements).
func emitNestedStructs(b *strings.Builder, fields []fieldInfo) {
	for _, f := range fields {
		if len(f.nestedFields) == 0 {
			continue
		}
		// Recurse first so inner types are defined before outer types.
		emitNestedStructs(b, f.nestedFields)

		fmt.Fprintf(b, "type %s struct {\n", f.nestedStructName)
		for _, nf := range f.nestedFields {
			writeFieldDoc(b, nf)
			fmt.Fprintf(b, "\t%s %s `json:\"%s,omitempty\"`\n\n", nf.goName, nf.goType, nf.jsonName)
		}
		b.WriteString("}\n\n")
	}
}

func writeFieldDoc(b *strings.Builder, f fieldInfo) {
	if f.description != "" {
		for _, line := range strings.Split(f.description, "\n") {
			fmt.Fprintf(b, "\t// %s\n", line)
		}
	}
}

func generateSpec(cfg Config, ds *dsInfo, ci CategoryRule) string {
	var b strings.Builder
	apiImport := fmt.Sprintf("%s/apis/observed/%s/%s", cfg.ModulePath, ci.DirName, cfg.APIVersion)
	tfdsPkg := fmt.Sprintf("%s/internal/controller/namespaced/observed/tfdatasource", cfg.ModulePath)

	fmt.Fprintf(&b, "/*\n%s\n*/\n\n// Code generated by generate-observed; DO NOT EDIT.\n\npackage %s\n\nimport (\n", cfg.CopyrightHeader, ci.DirName)

	if ds.isLegacySDK {
		needsFmt := false
		for _, f := range ds.forProviderFields {
			if strings.HasPrefix(f.goType, "*") || (f.goType != goTypeString) {
				needsFmt = true
				break
			}
		}
		if needsFmt {
			b.WriteString("\t\"fmt\"\n\n")
		}
		b.WriteString("\t\"github.com/crossplane/crossplane-runtime/v2/pkg/meta\"\n")
		b.WriteString("\t\"github.com/crossplane/crossplane-runtime/v2/pkg/resource\"\n")
		b.WriteString("\tsdkschema \"github.com/hashicorp/terraform-plugin-sdk/v2/helper/schema\"\n\n")
		fmt.Fprintf(&b, "\tv1alpha1 %q\n", apiImport)
		fmt.Fprintf(&b, "\ttfdatasource %q\n", tfdsPkg)
		b.WriteString(")\n\n")
		generateLegacySpec(&b, ds)
	} else {
		hasAtProvider := len(ds.atProviderFields) > 0
		if hasAtProvider {
			b.WriteString("\t\"context\"\n\n")
		}
		b.WriteString("\t\"github.com/crossplane/crossplane-runtime/v2/pkg/resource\"\n")
		if hasAtProvider {
			b.WriteString("\t\"github.com/hashicorp/terraform-plugin-framework/path\"\n")
		}
		b.WriteString("\t\"github.com/hashicorp/terraform-plugin-framework/tfsdk\"\n")
		b.WriteString("\t\"github.com/hashicorp/terraform-plugin-go/tftypes\"\n")
		b.WriteString("\n")
		fmt.Fprintf(&b, "\tv1alpha1 %q\n", apiImport)
		fmt.Fprintf(&b, "\ttfdatasource %q\n", tfdsPkg)
		b.WriteString(")\n\n")
		generateFrameworkSpec(&b, ds)
	}

	return b.String()
}

func generateLegacySpec(b *strings.Builder, ds *dsInfo) {
	fmt.Fprintf(b, "var %sSpec = tfdatasource.Spec{\n", ds.kindName)
	fmt.Fprintf(b, "\tDataSourceName: %q,\n", ds.tfName)
	fmt.Fprintf(b, "\tManagedKind:    v1alpha1.%s_GroupVersionKind,\n", ds.kindName)
	fmt.Fprintf(b, "\tNewManaged:     func() resource.Managed { return &v1alpha1.%s{} },\n", ds.kindName)
	b.WriteString("\tRead: tfdatasource.NewLegacyReadFn(\n")
	fmt.Fprintf(b, "\t\t%q,\n", ds.tfName)

	// toAttrs callback.
	b.WriteString("\t\tfunc(mg resource.Managed) map[string]string {\n")
	if len(ds.forProviderFields) > 0 {
		fmt.Fprintf(b, "\t\t\tcr := mg.(*v1alpha1.%s)\n", ds.kindName)
	}
	b.WriteString("\t\t\tattrs := map[string]string{}\n")
	for _, f := range ds.forProviderFields {
		switch {
		case strings.HasPrefix(f.goType, "*"):
			fmt.Fprintf(b, "\t\t\tif cr.Spec.ForProvider.%s != nil {\n", f.goName)
			fmt.Fprintf(b, "\t\t\t\tattrs[%q] = fmt.Sprintf(\"%%v\", *cr.Spec.ForProvider.%s)\n", f.tfName, f.goName)
			b.WriteString("\t\t\t}\n")
		case f.goType == goTypeString:
			fmt.Fprintf(b, "\t\t\tattrs[%q] = cr.Spec.ForProvider.%s\n", f.tfName, f.goName)
		default:
			fmt.Fprintf(b, "\t\t\tattrs[%q] = fmt.Sprintf(\"%%v\", cr.Spec.ForProvider.%s)\n", f.tfName, f.goName)
		}
	}
	b.WriteString("\t\t\treturn attrs\n\t\t},\n")

	// fromData callback.
	b.WriteString("\t\tfunc(mg resource.Managed, d *sdkschema.ResourceData) {\n")
	fmt.Fprintf(b, "\t\t\tcr := mg.(*v1alpha1.%s)\n", ds.kindName)
	b.WriteString("\t\t\tmeta.SetExternalName(cr, d.Id())\n")
	for _, f := range ds.atProviderFields {
		b.WriteString(generateFromResourceData(f))
	}
	b.WriteString("\t\t},\n\t),\n}\n")
}

func generateFrameworkSpec(b *strings.Builder, ds *dsInfo) {
	fmt.Fprintf(b, "var %sSpec = tfdatasource.Spec{\n", ds.kindName)
	fmt.Fprintf(b, "\tDataSourceName: %q,\n", ds.tfName)
	fmt.Fprintf(b, "\tManagedKind:    v1alpha1.%s_GroupVersionKind,\n", ds.kindName)
	fmt.Fprintf(b, "\tNewManaged:     func() resource.Managed { return &v1alpha1.%s{} },\n", ds.kindName)
	b.WriteString("\tRead: tfdatasource.NewFrameworkReadFn(\n")

	fmt.Fprintf(b, "\t\tnewDS%s,\n", ds.kindName)

	// toAttrs callback.
	if len(ds.forProviderFields) > 0 {
		b.WriteString("\t\tfunc(mg resource.Managed) map[string]tftypes.Value {\n")
		fmt.Fprintf(b, "\t\t\tcr := mg.(*v1alpha1.%s)\n", ds.kindName)
		b.WriteString("\t\t\tattrs := map[string]tftypes.Value{}\n")
		for _, f := range ds.forProviderFields {
			b.WriteString(generateToTFTypesValue(f))
		}
		b.WriteString("\t\t\treturn attrs\n\t\t},\n")
	} else {
		b.WriteString("\t\tfunc(_ resource.Managed) map[string]tftypes.Value {\n")
		b.WriteString("\t\t\treturn nil\n\t\t},\n")
	}

	// fromState callback.
	if len(ds.atProviderFields) > 0 {
		b.WriteString("\t\tfunc(mg resource.Managed, state tfsdk.State) {\n")
		fmt.Fprintf(b, "\t\t\tcr := mg.(*v1alpha1.%s)\n", ds.kindName)
		for _, f := range ds.atProviderFields {
			b.WriteString(generateFromState(f))
		}
		b.WriteString("\t\t},\n\t),\n}\n")
	} else {
		b.WriteString("\t\tfunc(_ resource.Managed, _ tfsdk.State) {},\n\t),\n}\n")
	}
}

func generateFromResourceData(f fieldInfo) string {
	// Handle nested struct types (List/Set of objects).
	if len(f.nestedFields) > 0 {
		return generateNestedFromResourceData(f)
	}
	switch f.goType {
	case goTypePtrString:
		return fmt.Sprintf("\t\t\tif v, ok := d.GetOk(%q); ok {\n\t\t\t\ts := v.(string)\n\t\t\t\tcr.Status.AtProvider.%s = &s\n\t\t\t}\n", f.tfName, f.goName)
	case goTypeString:
		return fmt.Sprintf("\t\t\tcr.Status.AtProvider.%s = d.Get(%q).(string)\n", f.goName, f.tfName)
	case goTypePtrInt64:
		return fmt.Sprintf("\t\t\tif v, ok := d.GetOk(%q); ok {\n\t\t\t\ti := int64(v.(int))\n\t\t\t\tcr.Status.AtProvider.%s = &i\n\t\t\t}\n", f.tfName, f.goName)
	case goTypeInt64:
		return fmt.Sprintf("\t\t\tcr.Status.AtProvider.%s = int64(d.Get(%q).(int))\n", f.goName, f.tfName)
	case goTypePtrFloat:
		return fmt.Sprintf("\t\t\tif v, ok := d.GetOk(%q); ok {\n\t\t\t\tf := v.(float64)\n\t\t\t\tcr.Status.AtProvider.%s = &f\n\t\t\t}\n", f.tfName, f.goName)
	case goTypeFloat64:
		return fmt.Sprintf("\t\t\tcr.Status.AtProvider.%s = d.Get(%q).(float64)\n", f.goName, f.tfName)
	case goTypePtrBool:
		return fmt.Sprintf("\t\t\tif v, ok := d.GetOk(%q); ok {\n\t\t\t\tb := v.(bool)\n\t\t\t\tcr.Status.AtProvider.%s = &b\n\t\t\t}\n", f.tfName, f.goName)
	case goTypeBool:
		return fmt.Sprintf("\t\t\tcr.Status.AtProvider.%s = d.Get(%q).(bool)\n", f.goName, f.tfName)
	default:
		return fmt.Sprintf("\t\t\t// TODO: complex type %s for %s\n", f.goType, f.tfName)
	}
}

func generateNestedFromResourceData(f fieldInfo) string {
	// Count how many scalar fields we can extract.
	hasScalarFields := false
	for _, nf := range f.nestedFields {
		if len(nf.nestedFields) == 0 && isScalarType(nf.goType) {
			hasScalarFields = true
			break
		}
	}

	var b strings.Builder
	fmt.Fprintf(&b, "\t\t\tif v, ok := d.GetOk(%q); ok {\n", f.tfName)
	fmt.Fprintf(&b, "\t\t\t\tvar items []v1alpha1.%s\n", f.nestedStructName)
	if f.isSet {
		b.WriteString("\t\t\t\tfor _, raw := range v.(*sdkschema.Set).List() {\n")
	} else {
		b.WriteString("\t\t\t\tfor _, raw := range v.([]interface{}) {\n")
	}
	fmt.Fprintf(&b, "\t\t\t\t\titem := v1alpha1.%s{}\n", f.nestedStructName)
	if hasScalarFields {
		b.WriteString("\t\t\t\t\tm := raw.(map[string]interface{})\n")
		for _, nf := range f.nestedFields {
			if len(nf.nestedFields) > 0 || !isScalarType(nf.goType) {
				continue
			}
			switch nf.goType {
			case goTypeString:
				fmt.Fprintf(&b, "\t\t\t\t\tif val, ok := m[%q].(string); ok {\n\t\t\t\t\t\titem.%s = val\n\t\t\t\t\t}\n", nf.tfName, nf.goName)
			case goTypePtrString:
				fmt.Fprintf(&b, "\t\t\t\t\tif val, ok := m[%q].(string); ok {\n\t\t\t\t\t\titem.%s = &val\n\t\t\t\t\t}\n", nf.tfName, nf.goName)
			case goTypeInt64:
				fmt.Fprintf(&b, "\t\t\t\t\tif val, ok := m[%q].(int); ok {\n\t\t\t\t\t\tv := int64(val)\n\t\t\t\t\t\titem.%s = v\n\t\t\t\t\t}\n", nf.tfName, nf.goName)
			case goTypePtrInt64:
				fmt.Fprintf(&b, "\t\t\t\t\tif val, ok := m[%q].(int); ok {\n\t\t\t\t\t\tv := int64(val)\n\t\t\t\t\t\titem.%s = &v\n\t\t\t\t\t}\n", nf.tfName, nf.goName)
			case goTypeBool:
				fmt.Fprintf(&b, "\t\t\t\t\tif val, ok := m[%q].(bool); ok {\n\t\t\t\t\t\titem.%s = val\n\t\t\t\t\t}\n", nf.tfName, nf.goName)
			case goTypePtrBool:
				fmt.Fprintf(&b, "\t\t\t\t\tif val, ok := m[%q].(bool); ok {\n\t\t\t\t\t\titem.%s = &val\n\t\t\t\t\t}\n", nf.tfName, nf.goName)
			case goTypeFloat64:
				fmt.Fprintf(&b, "\t\t\t\t\tif val, ok := m[%q].(float64); ok {\n\t\t\t\t\t\titem.%s = val\n\t\t\t\t\t}\n", nf.tfName, nf.goName)
			case goTypePtrFloat:
				fmt.Fprintf(&b, "\t\t\t\t\tif val, ok := m[%q].(float64); ok {\n\t\t\t\t\t\titem.%s = &val\n\t\t\t\t\t}\n", nf.tfName, nf.goName)
			}
		}
	} else {
		b.WriteString("\t\t\t\t\t_ = raw\n")
	}
	b.WriteString("\t\t\t\t\titems = append(items, item)\n")
	b.WriteString("\t\t\t\t}\n")
	fmt.Fprintf(&b, "\t\t\t\tcr.Status.AtProvider.%s = items\n", f.goName)
	b.WriteString("\t\t\t}\n")
	return b.String()
}

func isScalarType(goType string) bool {
	switch goType {
	case goTypeString, goTypePtrString, goTypeInt64, goTypePtrInt64,
		goTypeFloat64, goTypePtrFloat, goTypeBool, goTypePtrBool:
		return true
	default:
		return false
	}
}

