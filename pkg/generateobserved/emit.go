func generateExample(cfg Config, ds *dsInfo, ci CategoryRule) string {
	group := ci.GroupPrefix + cfg.GroupSuffix
	apiVersion := group + "/" + cfg.APIVersion

	var b strings.Builder
	fmt.Fprintf(&b, "apiVersion: %s\n", apiVersion)
	fmt.Fprintf(&b, "kind: %s\n", ds.kindName)
	b.WriteString("metadata:\n")
	fmt.Fprintf(&b, "  name: example-%s\n", ds.fileName)
	b.WriteString("  namespace: default\n")
	b.WriteString("spec:\n")
	b.WriteString("  forProvider:\n")

	if len(ds.forProviderFields) == 0 {
		b.WriteString("    {}\n")
	} else {
		for _, f := range ds.forProviderFields {
			fmt.Fprintf(&b, "    %s: %s\n", f.jsonName, exampleValue(f))
		}
	}

	b.WriteString("  providerConfigRef:\n")
	b.WriteString("    name: example\n")

	return b.String()
}

// exampleValue returns a placeholder value for an example manifest field.
func exampleValue(f fieldInfo) string {
	switch f.goType {
	case goTypeString, goTypePtrString:
		return fmt.Sprintf("\"example-%s\"", f.tfName)
	case goTypeInt64, goTypePtrInt64:
		return "0"
	case goTypeFloat64, goTypePtrFloat:
		return "0.0"
	case goTypeBool, goTypePtrBool:
		return "false"
	default:
		return "# TODO"
	}
}
